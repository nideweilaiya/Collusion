def _md_to_html(md: str) -> str:
    """简易 Markdown → HTML (不依赖 mistune)"""
    lines = md.split("\n")
    out = []
    in_list = False
    for line in lines:
        if line.startswith("## "):
            if in_list:
                out.append("</ul>")
                in_list = False
            out.append(f"<h2>{line[3:]}</h2>")
        elif line.startswith("### "):
            if in_list:
                out.append("</ul>")
                in_list = False
            out.append(f"<h3>{line[4:]}</h3>")
        elif line.startswith("# "):
            if in_list:
                out.append("</ul>")
                in_list = False
            out.append(f"<h1>{line[2:]}</h1>")
        elif line.startswith("- "):
            if not in_list:
                out.append("<ul>")
                in_list = True
            cls = ""
            if "BLOCKING" in line or "CRITICAL" in line:
                cls = ' class="risk-critical"'
            elif "WARNING" in line or "HIGH" in line:
                cls = ' class="risk-high"'
            out.append(f"<li{cls}>{line[2:]}</li>")
        elif line.startswith("---"):
            if in_list:
                out.append("</ul>")
                in_list = False
            out.append("<hr>")
        elif line.startswith("**") and "**" in line[2:]:
            if in_list:
                out.append("</ul>")
                in_list = False
            out.append(f"<p><strong>{line[2:line.index('**', 2)]}</strong>{line[line.index('**', 2)+2:]}</p>")
        elif line.strip():
            if in_list:
                out.append("</ul>")
                in_list = False
            out.append(f"<p>{line}</p>")
    if in_list:
        out.append("</ul>")
    return "\n".join(out)
