def render_decision_card(card_dict: dict, fmt: str = "md",
                         data_dir=None, task_id: str = "") -> dict:
    """将 DecisionCard 渲染为文件

    Args:
        card_dict: DecisionCard.to_dict() 的输出
        fmt: "md" | "html" | "both"
        data_dir: 数据目录路径
        task_id: 任务ID

    Returns:
        {"format": str, "markdown": str, "html": str}
    """
    from src.models import DecisionCard
    card = DecisionCard.from_dict(card_dict)

    if data_dir is None:
        data_dir = Path("data")
    if isinstance(data_dir, str):
        data_dir = Path(data_dir)

    output_dir = data_dir / "outputs" / (task_id or card.task_id or card.card_id)
    output_dir.mkdir(parents=True, exist_ok=True)

    paths = {"format": fmt}

    if fmt in ("md", "both"):
        md_content = card.to_markdown()
        md_content += _render_checkpoint_details(card)
        md_path = output_dir / "decision_card.md"
        md_path.write_text(md_content, encoding="utf-8")
        paths["markdown"] = str(md_path)

    if fmt in ("html", "both"):
        html_content = _card_to_html(card)
        html_path = output_dir / "decision_card.html"
        html_path.write_text(html_content, encoding="utf-8")
        paths["html"] = str(html_path)

    return paths


def _render_checkpoint_details(card) -> str:
    """渲染检查点详细结果"""
    if not card.checkpoint_results:
        return ""
    lines = ["", "---", "", "## 检查点详细结果", ""]
    for r in card.checkpoint_results:
        lines.append(f"### {r.get('checkpoint_id', '?')}")
        lines.append(f"- 严重性: {r.get('severity', '?')}")
        lines.append(f"- 置信度: {r.get('confidence', 0):.0%}")
        lines.append(f"- 风险评分: {r.get('risk_score', 0):.1%}")
        lines.append(f"- LLM调用: {r.get('llm_calls', 0)}")
        if r.get("findings"):
            lines.append("- 发现:")
            for f in r["findings"]:
                lines.append(f"  - [{f.get('type','?')}] {f.get('detail','')}")
        if r.get("uncertainty_flags"):
            lines.append(f"- 不确定项: {'; '.join(str(u) for u in r['uncertainty_flags'][:5])}")
        lines.append("")
    return "\n".join(lines)


